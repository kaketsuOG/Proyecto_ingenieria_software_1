    CELULAR_CLIENTE INT,
    NOMBRE_CLIENTE VARCHAR(30),
    APELLIDO_CLIENTE VARCHAR(30),
    DIRECCION_CLIENTE VARCHAR(60),
    PRIMARY KEY(CELULAR_CLIENTE)
);

CREATE TABLE PRODUCTO(
    COD_PRODUCTO INT,
    NOMBRE_PRODUCTO VARCHAR(30),
    PRECIO_PRODUCTO INT,
    PRIMARY KEY(COD_PRODUCTO)
);

CREATE TABLE CIUDAD_SUCURSAL(
    COD_CIUDAD_SUCURSAL INT,
    NOMBRE_CIUDAD VARCHAR(50),
    PRIMARY KEY(COD_CIUDAD_SUCURSAL)
);
CREATE TABLE SUCURSAL(
    COD_SUCURSAL INT,
    NOMBRE_SUCURSAL VARCHAR(200),
    COD_CIUDAD_SUCURSAL INT,
    CALLE_SUCURSAL VARCHAR(30),
    NRO_DIRECCION_SUCURSAL INT,
    PRIMARY KEY(COD_SUCURSAL),
    CONSTRAINT FOREIGN KEY(COD_CIUDAD_SUCURSAL) REFERENCES CIUDAD_SUCURSAL(COD_CIUDAD_SUCURSAL)
);

CREATE TABLE INVENTARIO(
    COD_INVENTARIO INT,
    COD_PRODUCTO INT,
    COD_SUCURSAL INT,
    CANTIDAD_TOTAL INT,
    CANTIDAD_DISPONIBLE INT,
    PRIMARY KEY(COD_INVENTARIO),
    CONSTRAINT FOREIGN KEY(COD_SUCURSAL) REFERENCES SUCURSAL(COD_SUCURSAL),
    CONSTRAINT FOREIGN KEY(COD_PRODUCTO) REFERENCES PRODUCTO(COD_PRODUCTO)
);

CREATE TABLE RESERVA(
    COD_RESERVA INT,
