    CONSTRAINT FOREIGN KEY(COD_DISPONIBILIDAD_FECHA) REFERENCES DISPONIBILIDAD_FECHA(COD_DISPONIBILIDAD_FECHA),
    CONSTRAINT FOREIGN KEY(COD_VEHICULO) REFERENCES VEHICULO(COD_VEHICULO)
    
);

CREATE TABLE PEDIDO(
    COD_PEDIDO INT,
    COD_PRODUCTO INT,
    COD_RESERVA INT,
    CANTIDAD_SOLICITADA INT,
    PRIMARY KEY(COD_PEDIDO),
    CONSTRAINT FOREIGN KEY (COD_PRODUCTO) REFERENCES PRODUCTO(COD_PRODUCTO),
    CONSTRAINT FOREIGN KEY(COD_RESERVA) REFERENCES RESERVA(COD_RESERVA)
);

CREATE TABLE HISTORIAL(
